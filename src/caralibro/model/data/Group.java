package caralibro.model.data;

public class Group {
	private Long id = null;
	private String name = null;
	
	public Group() {
	}
	
	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
